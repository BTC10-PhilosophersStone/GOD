- 経理
- 財務
- 人事
- 総務
- 生産管理
- 品質管理
- 物流
- 顧客管理
- 契約管理
- 情報システム
- プロジェクト管理

### 中分類（subCategory）

大分類を構成する業務プロセスを表す。

例

経理の場合

- 支払管理
- 請求管理
- 債権管理
- 債務管理
- 固定資産管理
- 月次決算
- 年次決算

購買の場合

- 発注管理
- 仕入管理
- サプライヤ管理

営業の場合

- 顧客管理
- 商談管理
- 見積管理
- 契約管理

### 小分類（minorCategory）

実際に行う具体的な業務作業を表す。

例

経理 → 支払管理

- 支払依頼登録
- 支払承認
- 振込処理
- 支払結果確認

営業 → 顧客管理

- 顧客情報登録
- 顧客情報更新
- 顧客情報照会

購買 → 発注管理

- 発注登録
- 発注変更
- 発注取消

