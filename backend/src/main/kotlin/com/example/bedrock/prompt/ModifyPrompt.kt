### Why
目的を表すキーワードへ変換する

例：
"問い合わせ対応業務の自動化"
↓
"問い合わせ対応自動化"

### How
手段・技術・方式を抽出する

例：
"AIエージェント（音声ガイド／チャットボット）の活用"
↓
"AIエージェント,音声ガイド,チャットボット"

### What_Why

- 元のWhat_Whyは使用しない
- WhatとWhyから再生成する
- Whyを目的・背景として扱う
- Whatを実施内容として扱う
- 「何のために、何を行うのか」が分かる簡潔な文章を生成する
- テンプレートは固定しない
- 自然な業務表現で記述する
- 「です・ます調」は使用しない


