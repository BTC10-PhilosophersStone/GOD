# 共通ルール

- 出力形式（JSON構造）は変更しない
- キー名は変更しない
- JSON以外の説明文は出力しない
- 値は内容を表す重要な情報のみを残す
- 「です」「になります」「する」「した」「している」などの語尾は削除する
- 「を」「に」「で」「と」「が」「の」「として」などの助詞は削除する
- 接続詞・修飾語・冗長な説明は削除する
- 日付、場所、人名、組織名は保持する
- 文章ではなくキーワードまたは短い名詞句へ変換する
- 空白は最小限とする

